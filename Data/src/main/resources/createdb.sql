CREATE TABLE TEXTURES (
TID INTEGER NOT NULL GENERATED ALWAYS AS IDENTITY (START WITH 1, INCREMENT BY 1),
NAME varchar(30) NOT NULL,
FILENAME varchar(50) NOT NULL,
UNIQUE(TID, NAME),
PRIMARY KEY(TID));

CREATE TABLE ENTITIES (
EID INTEGER NOT NULL GENERATED ALWAYS AS IDENTITY (START WITH 1, INCREMENT BY 1),
NAME varchar(30),
TEXTURE_ID INTEGER NOT NULL,
X_POS FLOAT,
Y_POS FLOAT,
Z_POS FLOAT,
X_SIZE FLOAT DEFAULT 1.0,
Y_SIZE FLOAT DEFAULT 1.0,
Z_SIZE FLOAT DEFAULT 1.0,
TYPE INTEGER,
UNIQUE(EID),
PRIMARY KEY(EID, TEXTURE_ID));



